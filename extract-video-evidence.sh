## 1. 风格视觉指纹

用户提供截图或视频时，先用中文提取：

| 维度 | 识别内容 |
|---|---|
| 媒介 | 3D、2D、定格或混合 |
| 角色 | 年龄、体型、头身比、五官夸张程度 |
| 写实程度 | 卡通、半写实、高写实 |
| 材质 | 皮肤、眼睛、毛发、服装、金属、塑料等 |
| 灯光 | 主光、轮廓光、阴影、曝光 |
| 色彩 | 主色、辅助色、饱和度、对比度 |
| 背景 | 舞台、真实环境、抽象空间、系统界面 |
| 镜头 | 景别、视角、焦段感、景深 |
| 构图 | 主体位置、前中后景、留白 |
| 特效 | 发光、粒子、HUD、扫描、失焦、运动模糊 |
| 字幕 | 字体、颜色、描边、位置；默认后期添加 |
| 风险 | 跨景别与跨段最容易漂移的属性 |

未提供参考时使用 `pattern-library.md` 的默认视觉风格。

## 2. 全局母版

第二阶段开始时先输出三个可复用模块。

### 全局风格母版

```text
生成竖版9:16的[媒介与风格]画面。

所有画面必须属于同一部作品和同一渲染体系。统一保持[写实程度]、[角色比例]、[皮肤/眼睛/毛发/服装材质]、[灯光]、[背景]、[色彩]、[景深]和[特效体系]。

景别、尺度和空间可以变化，但视觉媒介、角色建模、材质与光影不能改变。
```

### 角色身份母版

逐项锁定：角色数量、年龄、性别表达、脸型、肤色、眼睛、眉毛、鼻子、发型、体型、服装、鞋子、配饰、气质和标志性特征。多个角色分开编号。

### 负面约束母版

只写当前风格最可能发生的错误，例如人物变脸、服装漂移、极端特写变真人、2D图标混入3D世界、产品比例变化、错误手指、文字乱码、背景色温变化。

## 3. 图片Prompt

每张关键帧必须是可独立复制的完整中文Prompt，不要只写与上一张的差异。

### 标头

```text
关键帧[编号]｜[时间]｜[视觉状态]
引用：[需要上传的参考图片]
用途：[为何需要独立关键帧]
```

### 完整结构

```text
生成一张竖版9:16的[完整风格描述]画面。

【一致性】
参考上传的[图片编号]，严格保持[角色/产品/风格]完全一致。景别变化不是风格变化；极端微距仍使用相同媒介、角色模型、材质与灯光。

【人物/主体】
写清身份、外观、服装、姿势、表情、视线、手部和身体重心。

【当前动作状态】
描述关键帧锁定的动作起点或终点，不写整段动画过程。

【构图与镜头】
写清景别、视角、主体位置、画面占比、前中后景、焦点和字幕留白。

【道具与空间】
写清数量、位置、方向、层次、材质和与主体的关系。不要把后续才进入的全部物体提前放进首帧。

【灯光与材质】
重复必要的统一光线、主色、背景和材质约束。

【文字或对白】
默认不在图片中生成字幕。必须出现的画面文字使用目标市场语言，默认英语，并逐字写出。只有脚本明确包含对话时才写对白。

【禁止事项】
列出角色漂移、媒介漂移、错误肢体、文字乱码、遮挡脸部、额外人物和当前画面特有错误。
```

### 图片引用规则

- 第一张建立风格和角色母版；
- 后续人物帧引用第一张或最近的高质量同景别角色帧；
- 极端特写仍引用角色母版；
- 产品首次出现时引用产品原图；
- 新空间引用最近的角色帧＋空间风格帧；
- 跨10秒段的第一张原则上复用上一段末帧。

## 4. 视频Prompt

每个10秒段输出一条完整中文Prompt。视频生成模型只看到上传关键帧，因此Prompt不得依赖未上传的参考视频。

### 开头

```text
使用上传的[数量]张关键帧，按照图片1、图片2……的顺序，生成一条[时长]秒、竖版9:16的[风格]动画视频。

关键帧时间：图片1=[时间]，图片2=[时间]……

本段表达：[独立说明这一段的叙事目标和最终状态]。
```

不要写“复刻参考视频”“按照原视频逻辑”或其他模型无法验证的目标。

### 全片统一要求

写清：

- 同一视觉媒介与渲染体系；
- 同一角色身份、服装和身体比例；
- 同一产品外观和比例；
- 同一灯光、背景、材质和色彩；
- 允许变化的是动作、景别、构图、道具、特效、尺度和空间；
- 极端特写是景别切换，不是风格切换。

### 时间轴

按实际视觉事件分段，不机械平均：

```text
一、0.0–1.2秒：[主动作]
人物如何移动；镜头如何变化；道具何时进入；结束状态是什么。

二、1.2–2.5秒：[主动作]
……
```

每段只安排一个主动作。道具分批进入，标注方向、速度和前中后景。硬切明确写“不得变形、溶解或风格融合”。

### 高频节奏要求

```text
每0.4–0.8秒至少改变人物、镜头、构图、道具、特效、尺度或空间中的一条；字幕变化不计入主要变化。

每1.5–3秒出现一次中变化。本段至少安排一次强注意力重置。强变化后保留0.3–0.7秒识别窗口。

不要让所有元素同时剧烈运动，每个阶段只保留一个主动作。
```

### 目标语言

Prompt主体使用中文。存在角色对话、口型或画面文字时，逐字写出目标市场语言，默认英语：

```text
人物看向镜头，用自然的美国英语说：“[英文台词]”。口型与英文准确同步，不增加其他台词。
```

角色对话不改成旁白。

### 禁止事项

根据当前段列出：人物变脸、服装漂移、产品变形、风格切换、额外人物、复杂背景、错误肢体、文字乱码、脸部遮挡、所有物体同时出现、关键帧之间错误变形等。

## 5. 声音模块

放在视频Prompt末尾，长度应明显短于主视觉时间轴。

```text
【声音要求】

不生成背景音乐和旁白。只有脚本明确包含角色对话时，才生成目标市场语言对白。

人物动作加入自然的服装、脚步、抓握和呼吸拟音；物体进入、旋转、掠过和碰撞时加入与材质、方向和速度同步的声音。

发光、扫描、HUD、电路、粒子和能量动画加入简洁的点亮、扫描、脉冲和流动音；充能、断电、失焦、崩塌和恢复加入短促的状态音；推近、硬切、遮挡和空间穿越加入对应的转场音。

只标注关键声音节点。所有音效必须有明确触发画面、开始和结束，不持续堆叠，不遮挡后期配音。
```

如果某段只有普通动作，不强行加入科技音效。声音跟随视觉语义，不为了丰富而堆砌。

## 6. 交付与检查

每个10秒段依次输出：

1. 关键帧清单与时间；
2. 每张关键帧的完整图片Prompt；
3. 一条完整视频Prompt；
4. 跨段桥接说明。

长视频第二阶段优先逐段交付，避免一次输出几十条Prompt导致截断。用户明确要求整批输出时可以一次完成。

检查：

- 图片Prompt是否自包含？
- 每张是否重复必要的一致性约束？
- 极端特写是否仍是同一风格？
- 视频Prompt是否只引用实际上传的关键帧？
- 是否描述完整时间轴而不是外部目标？
- 对白和画面文字是否使用目标语言？
- 是否禁止背景音乐和旁白？
- 音效是否包含动画特效与转场，而不只是真实动作？
- 声音模块是否简洁？
